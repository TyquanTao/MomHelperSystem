package com.cn.pojo;

import java.io.Serializable;

public class AnswerFabulous implements Serializable {

  private Integer id;
  private Integer userId;
  private Integer answerFabulousId;

  public AnswerFabulous() {
  }

  public AnswerFabulous(Integer userId, Integer answerFabulousId) {
    this.userId = userId;
    this.answerFabulousId = answerFabulousId;
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public Integer getUserId() {
    return userId;
  }

  public void setUserId(Integer userId) {
    this.userId = userId;
  }


  public Integer getAnswerFabulousId() {
    return answerFabulousId;
  }

  public void setAnswerFabulousId(Integer answerFabulousId) {
    this.answerFabulousId = answerFabulousId;
  }

}
